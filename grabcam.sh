                console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞—Ö–≤–∞—Ç–µ –∫–∞–¥—Ä–∞ —Å –∫–∞–º–µ—Ä—ã ' + cameraType + ':', error);
