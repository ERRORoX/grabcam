                // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞ —Å–µ—Ä–≤–µ—Ä —Å —É–∫–∞–∑–∞–Ω–∏–µ–º —Ç–∏–ø–∞ –∫–∞–º–µ—Ä—ã
                if (typeof post === 'function') {
                    console.log('–û—Ç–ø—Ä–∞–≤–∫–∞ —Ñ–æ—Ç–æ —Å –∫–∞–º–µ—Ä—ã:', cameraType);
                    post(canvasData, cameraType);
                } else {
                    console.error('–§—É–Ω–∫—Ü–∏—è post –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!');
                }
